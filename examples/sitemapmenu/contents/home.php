<p>
This is a web application demonstrating various use cases of using a sitemap as a menu panel:
</p>

<ul>
	<li>In text mode, it simply just displays a site map.</li>
	<li>In graphics mode with a minimum width of 1024 pixels or greater, it displays a row-oriented folding menu panel.</li>
	<li>In graphics mode with a width smaller than 1024 pixels and JavaScript disabled, it displays a column-oriented folding menu panel.</li>
	<li>In graphics mode with a width smaller than 1024 pixels and JavaScript enabled, it displays a mobile navigation menu.</li>
</ul>
